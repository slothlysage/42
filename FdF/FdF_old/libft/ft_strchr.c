#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*t;

	t = (char*)s;
	while (*t)
	{
		if (*t == c)
			return (t);
		t++;
	}
	if (c == '\0')
		return (t);
	return (NULL);
}
