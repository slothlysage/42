#include "fillit.h"

void	set_l(t_brd *b, t_tet *t)
{
	int		i;

	while (t)
	{
		i = 0;
		while (i++ <= 3)
		{
			if (t->h[i] == 1)
				t->l[i] = 1;
			if (t->h[i] == 3 || t->h[i] == 4 || t->h[i] == 5)
				t->l[i] = (t->h[i] + b->l - 5);
		}
		t->l[2] += t->l[1];
		t->l[3] += t->l[2];
		t = t->next;
	}
}
