#include "fillit.h"

int		validate(t_tet *t)
{
	int		c;
	int		i;
	int		h;

	i = -1;
	h = 0;
	c = 0;
	while (++i < 20 && (t->p[i] == '.' || t->p[i] == '#' || t->p[i] == '\n'))
	{
		if ((((i + 1) % 5 == 0) && t->p[i] != '\n'))
			return (-1);
		if (t->p[i] == '#')
		{
			if (i < 20)
				c += (((t->p[i + 1]) == '#') ? (1) : (0));
			if (i < 16)
				c += (((t->p[i + 5]) == '#') ? (1) : (0));
			if (i > 0)
				c += (((t->p[i - 1]) == '#') ? (1) : (0));
			if (i > 4)
				c += (((t->p[i - 5]) == '#') ? (1) : (0));
			h++;
		}
	}
	return ((h == 4 && (c == 6 || c == 8)) ? (1) : (0));
}
