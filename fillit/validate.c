#include "fillit.h"

int		validate_tet(char *t)
{
	int		c;
	int		i;
	int		h;

	i = -1;
	h = 0;
	c = 0;
	while (++i < 20 && (t[i] == '.' || t[i] == '#' || t[i] == '\n'))
	{
		if ((((i + 1) % 5 == 0) && t[i] != '\n'))
			return (-1);
		if (t[i] == '#')
		{
			if (i < 20)
				c += (((t[i + 1]) == '#') ? (1) : (0));
			if (i < 16)
				c += (((t[i + 5]) == '#') ? (1) : (0));
			if (i > 0)
				c += (((t[i - 1]) == '#') ? (1) : (0));
			if (i > 4)
				c += (((t[i - 5]) == '#') ? (1) : (0));
			h++;
		}
	}
	return ((h == 4 && (c == 6 || c == 8)) ? (1) : (0));
}

int		validate_dot(char *t)
{
	int		d;
	int		i;

	d = 0;
	i = -1;
	while (t[++i])
	{
		if (t[i] == '.')
			d++;
	}
	return ((d == 12) ? (1) : (0));
}

int		validate_map(t_brd *b, char a)
{
	int		c;
	int		i;
	int		h;

	i = (b->i - 1);
	h = 0;
	c = 0;
	while ((++i <= (b->l * b->l)) && h < 4 && (b->m[i] == '.' ||
			(b->m[i] >= 'A' && b->m[i] <= 'Z')))
	{
		if (b->m[i] == a)
		{
			if (i % b->l < (b->l - 1))
				c += (((b->m[i + 1]) == a) ? (1) : (0));
			if (i <= ((b->l * (b->l - 1)) - 1))
				c += (((b->m[i + b->l]) == a) ? (1) : (0));
			if (i % b->l > 0)
				c += (((b->m[i - 1]) == a) ? (1) : (0));
			if (i >= b->l)
				c += (((b->m[i - b->l]) == a) ? (1) : (0));
			h++;
		}
	}
	return ((h == 4 && (c == 6 || c == 8)) ? (1) : (0));
}
