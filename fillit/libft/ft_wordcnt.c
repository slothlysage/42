#include "libft.h"

int		ft_wordcnt(const char *s, char c)
{
	char	*t;
	int		r;

	t = (char*)s;
	r = 0;
	while (*t)
	{
		if ((*t != c) && ((*(t + 1) == c) || (!(*(t + 1)))))
			r++;
		t++;
	}
	return (r);
}
