#include "fillit.h"

void	trim(t_tet *t)
{
	while (t)
	{
		while (*(t->p))
		{
			while (*(t->p++) != '#')
				t->h1++;
			while (*(t->p++) != '#')
				t->h2++;
			while (*(t->p++) != '#')
				t->h3++;
			while (*(t->p++) != '#')
				t->h4++;
		}
		t = t->next;
	}
}
