#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	const char	*s;
	char		*d;

	s = src;
	d = dst;
	while (*s)
	{
		*d = *s;
		d++;
		s++;
	}
	*d = '\0';
	return (dst);
}
