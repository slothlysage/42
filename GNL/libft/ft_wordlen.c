#include "libft.h"

int		ft_wordlen(const char *s, char c)
{
	char	*t;
	int		r;

	if (s != NULL)
	{
		r = 0;
		t = (char*)s;
		while (*t == c)
			t++;
		while (*t++ != c)
			r++;
		return (r);
	}
	return (0);
}
