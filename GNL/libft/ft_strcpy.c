#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char		*s;
	char		*d;

	s = (char*)src;
	d = dst;
	while (*s)
		*d++ = *s++;
	*d = '\0';
	return (dst);
}
