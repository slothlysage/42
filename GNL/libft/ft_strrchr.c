#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*t;

	t = ((char*)s + ft_strlen(s));
	if (c == '\0')
		return (t);
	while ((t - s) >= 0)
	{
		if (*t == c)
			return (t);
		t--;
	}
	return (NULL);
}
