#include "libft.h"

int		ft_atoi(const char *s)
{
	unsigned int	r;
	int				n;

	r = 0;
	while (*s == ' ' || *s == '\t' || *s == '\v' || *s == '\r'
			|| *s == '\n' || *s == '\f')
		s++;
	n = ((*s == '-') ? (-1) : (1));
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
		r = (r * 10) + (*s++ - '0');
	return (n * r);
}
